import turtle as t

def draw():
    t.color('black','red')
    t.begin_fill()
    t.penup()
    t.goto (50,50)
    t.pendown()
    t.right(45)
    t.goto(100,0)
    t.left(90)
    t.fd(120)
    t.circle(50,225)
    t.penup()
    t.goto(0,0)
    t.pendown()
    t.left(135)
    t.fd(120)
    t.circle(50,225)
    t.seth(90)
    t.circle(50,225)
    t.fd(121)
    t.end_fill()
    t.left(56)
    t.penup()
    t.goto(-210,40)
    t.pendown()
    t.goto(0,80)
    t.penup()
    t.goto(160,110)
    t.pendown()
    t.goto(320,140)
    t.done()

if __name__ == '__main__':
    draw()